const ALL_MAPS: Record<string, Record<string, TeamColors>> = {
  NBA, NFL, MLB, NHL,
  Soccer: {},
}

const FALLBACK: TeamColors = { primary: '#1a1d26', secondary: '#2a2f3e' }

export function getTeamColors(teamName: string, sport: string): TeamColors {
  const map = ALL_MAPS[sport] || {}
  // Try exact match first
  if (map[teamName]) return map[teamName]
  // Try partial match (last word of team name, e.g. "Lakers" from "Los Angeles Lakers")
  const words = teamName.split(' ')
  for (let i = 1; i <= words.length; i++) {
    const suffix = words.slice(i).join(' ')
    if (suffix && map[suffix]) return map[suffix]
  }
  return FALLBACK
}
